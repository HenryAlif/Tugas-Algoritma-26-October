// Penggunaan Notasi di Depan Variabel
#include <stdio.h>
#include <conio.h>
void main()
{
    int a = 8, b = 4;
    printf(" Nilai A = %d", a);
    printf("\n Nilai ++A = %d", ++a);
    printf("\n Nilai A =%d", a);
    printf("\n Nilai B =%d", b);
    printf("\n Nilai --B =%d", --b);
    printf("\n Nilai B =%d", b);
    getch();
}

